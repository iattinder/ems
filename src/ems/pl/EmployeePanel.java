    private void afterSave()
    {
    al=EMSServices.getAllEmployees();
    btnadd.setEnabled(true);
    btnedit.setEnabled(true);
    btnsave.setEnabled(false);
    
    if(addeditflag==-1)
    {
    showRecord(count);
    }
    
    if(addeditflag==1)
    {
    int a=al.size()-1;
    showRecord(a);
    }
    }
    
    private void showRecord(int count)
    {
    
       EmpBean objBean= al.get(count);
       emplabel.setText(String.valueOf(objBean.getEmpid()));
       ddlemptype.setSelectedItem(objBean.getEmptype());
       txtusername.setText(objBean.getUsername());
       txtpassword.setText(objBean.getPassword());
       txtname.setText(objBean.getName());
       txtdob.setText(objBean.getDob());
       taadress.setText(objBean.getAddress());
       txtemail.setText(objBean.getEmail());
       
       if(count!=0)
       {
           btnprevious.setEnabled(true);
           btnfirst.setEnabled(true);
       }
       else
       {
           btnprevious.setEnabled(false);
       }   
       
       if(objBean.isGender())
       {
           rbmale.setSelected(true);
       }
       else
       {
           rbfemale.setSelected(true);
       }
       
       if(objBean.isEmpstatus())
       {
       rbactive.setSelected(true);
       }
       else
       {
       rbinactive.setSelected(true);}
       
       enableAll(false);
    }
    

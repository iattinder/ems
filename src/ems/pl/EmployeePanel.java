    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed

        EmpBean objbean=new EmpBean();
        objbean.setEmpid(Integer.valueOf(emplabel.getText()));
        objbean.setUsername(txtusername.getText());
        objbean.setPassword(txtpassword.getText());
        objbean.setEmail(txtemail.getText());
        if(rbactive.isEnabled())
        {
        objbean.setEmpstatus(true);
        }
        else
        {
            objbean.setEmpstatus(false);
        }
        objbean.setAddress(taadress.getText());
        objbean.setName(txtname.getText());
        
        if(rbmale.isEnabled())
        {
        objbean.setGender(true);
        }
        else
        {
            objbean.setGender(false);
        }
        
        objbean.setDob(txtdob.getText());
        
        objbean.setEmptype(String.valueOf(ddlemptype.getSelectedItem()));
        
        
        if(Integer.valueOf(emplabel.getText())==(EMSServices.maxId()+1))

        {
            if(EMSServices.addEmployee(objbean))
            {
            
                JOptionPane.showMessageDialog(btnnext, "Record Added", "Message!", HEIGHT);
            }
            else
            {
                JOptionPane.showMessageDialog(btnnext, "Record Not Added", "Warning!", HEIGHT);
            }
            
        
        }
        
        else
        {
            
            if(EMSServices.updateEmployee(objbean))
            {
            
                JOptionPane.showMessageDialog(btnnext, "Record Updated", "Message!", HEIGHT);
            }
            else
            {
                JOptionPane.showMessageDialog(btnnext, "Record Not Updated", "Warning!", HEIGHT);
            }
            
        
        }

        
        afterSave();
        
        
        
        
// TODO add your handling code here:
    }//GEN-LAST:event_btnsaveActionPerformed

